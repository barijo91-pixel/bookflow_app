<ol>
    <li>이용자는 회사가 제공하는 결제수단(신용카드, 계좌이체, 간편결제 등)을 통해 교재 대금을 결제할 수 있습니다.</li>
    <li>결제 대금의 정산은 회사가 정한 정산 주기 및 방법에 따릅니다.</li>
    <li>결제·정산에 관한 세부 사항 및 취소·환불은 "취소·환불정책"에 따릅니다.</li>
</ol>

