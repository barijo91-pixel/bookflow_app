<h2>제2조 (정의)</h2>
<ol>
    <li>"서비스"란 회사가 제공하는 교재 도매·유통 중개, 주문·결제·정산 및 이에 부수하는 일체의 온라인 서비스를 말합니다.</li>
    <li>"이용자"란 서비스에 접속하여 이 약관에 따라 회사가 제공하는 서비스를 이용하는 총판·영업자·학원·학부모 등을 말합니다.</li>
    <li>"회원"이란 회사에 개인 또는 사업자 정보를 제공하여 회원등록을 하고 서비스를 이용하는 자를 말합니다.</li>
</ol>

<h2>제3조 (약관의 효력 및 변경)</h2>
<ol>
    <li>이 약관은 서비스 화면에 게시하거나 기타의 방법으로 이용자에게 공지함으로써 효력이 발생합니다.</li>
    <li>회사는 관련 법령을 위배하지 않는 범위에서 이 약관을 변경할 수 있으며, 변경 시 적용일자 및 변경사유를 명시하여 시행일 7일 이전(이용자에게 불리한 변경은 30일 이전)부터 공지합니다.</li>
</ol>

<h2>제4조 (서비스의 제공)</h2>
<ol>
    <li>회사는 교재 정보 제공, 주문·중개, 결제·정산, 배송 정보 관리 등의 서비스를 제공합니다.</li>
    <li>회사는 서비스의 내용·운영상 또는 기술상 필요에 따라 제공하는 서비스의 전부 또는 일부를 변경할 수 있습니다.</li>
    <li>서비스는 연중무휴 24시간 제공함을 원칙으로 하나, 시스템 점검·교체·고장, 통신 두절 등의 사유가 발생한 경우 일시 중단될 수 있습니다.</li>
</ol>

<h2>제5조 (회원가입 및 이용계약)</h2>
<ol>
    <li>이용계약은 이용자가 약관에 동의하고 회원가입을 신청한 후 회사가 이를 승낙함으로써 성립합니다.</li>
    <li>회사는 타인의 정보를 도용하거나 허위 정보를 기재한 경우, 기타 회사가 정한 이용신청 요건을 충족하지 못한 경우 승낙을 거부하거나 사후에 이용계약을 해지할 수 있습니다.</li>
</ol>

<h2>제6조 (이용자의 의무)</h2>
<ol>
    <li>이용자는 관계 법령, 이 약관의 규정, 이용안내 및 서비스상 공지한 주의사항을 준수하여야 합니다.</li>
    <li>이용자는 본인의 계정 정보를 선량한 관리자의 주의로 관리하여야 하며, 제3자가 이를 이용하도록 하여서는 안 됩니다.</li>
    <li>이용자는 서비스를 이용하여 얻은 정보를 회사의 사전 승낙 없이 복제·전송·출판·배포·방송 기타의 방법으로 영리목적에 이용하거나 제3자에게 이용하게 하여서는 안 됩니다.</li>
</ol>

